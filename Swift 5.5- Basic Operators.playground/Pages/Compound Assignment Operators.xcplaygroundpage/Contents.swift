import Foundation

//Compound Assignment Operators (복합 할당 연산자)
//스위프트는 ‘할당 연산 (=)’ 을 다른 연산과 조합한 복합 할당 연산자 를 제공합니다. 한 가지 예는 ‘더하기 할당 연산자 (addition assignment operator; +=)’ 입니다:

var a = 1
a += 1

//‘복합 할당 연산자’ 는 값을 반환하지 않습니다. 예를 들어, let b = a += 2 처럼 작성할 수 없습니다

//Property Wrappers (속성 포장)
//속성 포장 (property wrapper)’ 은 속성 저장 방법을 관리하는 코드와 속성을 정의하는 코드 사이에 ‘구분 계층 (layer of seperation)’ 을 추가합니다.
//예를 들어, ‘쓰레드-안전성 검사’ 를 제공하거나 실제 자료를 데이터베이스에 저장하는 속성을 가진 경우, 해당 코드를 모든 속성마다 작성해야 합니다.
//속성 포장을 사용할 때는, 포장을 정의할 때 관리 코드를 한 번 작성한 다음, 해당 관리 코드를 여러 속성에 적용함으로써 재사용합니다.



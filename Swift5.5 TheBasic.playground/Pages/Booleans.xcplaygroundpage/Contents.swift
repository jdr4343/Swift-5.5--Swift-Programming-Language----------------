import Foundation

//Booleans (불리언; 논리 값)
//스위프트는, Bool 이라는, 기본적인 불리언 (Boolean) 타입을 가지고 있습니다. ‘불리언’ 값은 논리 값 (logical) 이라고도 하는데, 늘true 또는 false 만 될 수 있기 때문입니다.

let orangesAreOrange = true
let turnipsAreDelicious = false

let SomeArray = [1, 2, 3, 4]
//불리언 값은 특히 if 문 같은 조건 구문과 작업할 때 아주 유용합니다:
if SomeArray.count < 2 {
    print(true)
} else {
    print(false)
}
